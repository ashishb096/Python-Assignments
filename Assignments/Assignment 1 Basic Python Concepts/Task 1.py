#Write a program to input two numbers from user and perform basic mathematical operations on these two numbers:
#Taking two numbers as input from user and storing the values as an integer in variable a and b respectively.
a=int(input('Enter the first number: '))
b=int(input('Enter the second number: '))
#Addition:
add = a + b
print('Addition: ',add)
#Subtraction:
sub = a - b
print('Subtraction: ',sub)
#Multiplication:
mul = a * b
print('Multiplication: ', mul)
#Division
div = a / b
print('Division: ',div)


